            	 <h1>Illowa Irregulars Cowboy Action Shooting</h1>
                	<p>Welcome to the Illowa Irregulars  Cowboy Action Shooting home page.  The  Illowa Irregulars are the Cowboy Action Shooting group of the Milan Rifle  Club.  We have monthly competitions from  April through November.  <a href="docs/2020-Cowboy-Schedule.pdf" target="_blank">Please see the  schedule</a> for the specific dates.  In  addition to our normal monthly matches, we have our <a href="fall-round-up.php">annual 3-day match (Fall  Round-Up</a>) the last weekend in September each year.  This is a pre-registration shoot (application is available on the Fall Round-Up page).</p>
<p><br />
   	    The governing body of Cowboy Action Shooting is the Single Action Shooting Society (SASS). While you do not have to be a member of <a href="http://sassnet.com/" target="_blank">SASS</a> to shoot at our club, we do follow all SASS rules so it is a good idea to be familiar with those rules. In addition to firearms requirements (see below), there are also dress requirements. You must wear long pants (jeans are OK) and a long sleeved shirt (a work shirt of some kind is fine) and, of course, cowboy boots and a cowboy hat.</p>
                	<p><br />
               	    A typical cowboy match consists of 6 stages. You travel with your &quot;posse&quot; (normally a group of approximately 15 other shooters) and shoot each stage. A scenario, which explains to the posse the order in which the guns are shot and the order in which the targets are engaged, is read. Penalties are imposed for shooting out of order or for missing a target. Firearms required to shoot our matches are pre-1900 (or replicas) firearms, i.e. 2 single action pistols (.32 through .45 caliber), one lever or pump action rifle chambered for pistol caliber ammunition (.32 through .45 caliber), and one shotgun (side-by-side or 97 pump or 87 lever) in 10 or 12 gauge.  All ammunition must be lead bullets only (no jacketed ammunition).</p>
