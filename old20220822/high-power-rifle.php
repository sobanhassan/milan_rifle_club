            	 <h1>High Power Rifle</h1>
                	<h3>High power rifle competition is not only a challenge, but also an enjoyable day spent with fellow shooters at the range.</h3>
                	<p>High power shooting is a challenging and competitive sport. Whether you are a novice or an experienced shooter, there is a tremendous amount of camaraderie on the range. Some shooters focus on participating in local shoots while others strive to go to the Nationals, which take place each year in Ohio, and compete with the top shooters in the nation. The sport is open to anyone interested in competitive shooting…all are welcome…most importantly, show up and give it a try!</p>
                	<p>In high power shooting, paper targets mounted on frames are used. Ranges vary from 200, 300 or 600 yards away from the target. There are different shooting positions during a match: standing, sitting, and prone (lying down on belly).</p>
                	<h3>Just getting started?</h3>
                	<p>At first all you will need to bring is a center fire rifle capable of holding at least 5 rounds of ammunition with metallic sights, safe ammunition, hearing protection, and a steady hand! In recent history a lot of AR15s have been sold, which is a good rifle to begin high power shooting. Don't be intimidated, just bring your equipment and come participate in a match. If you have questions all you need to do is ask. The high power community is filled with a lot of great people who are more than willing to help.</p>
                	<h3>Becoming a competitive shooter</h3>
                	<p>As you become more involved in the competitive shoots, there is more required equipment than just a rifle with a sling. The list includes: spotting scope with stand, shooting coat, mat to shoot prone position, shooting stool, offhand ammo holder, data book to track shots, and glove for your non-trigger hand. As you compete you will see what other shooters use to be more competitive and choose the items that work best for you.</p>
                	<p>Highpower offers shooters the ability to improve their marksmanship skills and to hit a target with precision. While competitive in nature, there are rarely observers watching the sport. The shooter competes for the shear prestige of winning rather than trophies or large cash prizes. Just a bunch of guys and gals with a wide range of skills and classifications who enjoy shooting high power rifles.</p>
                	<h3>Helpful Resources</h3>
                	<p>For more information about High Power Rifle Competition go to the National Rifle Association (NRA) website at <a href="http://competitions.nra.org/how-to-get-started/high-power-rifle-competition.aspx" target="_blank">http://competitions.nra.org/how-to-get-started/high-power-rifle-competition.aspx</a>. The NRA website includes detailed information about the types of courses, necessary equipment, match scores, and much more!</p>
                	<p>Illinois State Rifle Association (ISRA) High Power website at <a href="http://www.illinoishighpower.org/" target="_blank">http://www.illinoishighpower.org/</a> provides information about high power shooting, ranges, 10-state schedule, and tournaments.</p>
