            	 <h1>Milan Rifle Club Youth Pistol Program</h1>
                	<p>Milan Rifle Club Youth Pistol Program The Scholastic Shooting Sports Foundation (SSSF) is the national governing body for youth speed shooting on steel targets. For Pistols, their program is called the Scholastic Pistol Program (SPP). The 501c3 organization Youth Shooting Sports Association (YSSA) is the coordinator for Milan Rifle Club (MRC) and supplies the certified instructors, coaches, and range safety officers for the practices and events that are held at the facility.</p>
                	<p>The program is open to all youth ages 12 and up until they graduate from college. You will be required to join the SPP Program for fee of $20 and the program runs year round. YSSA Practices are held Tuesday night's at MRC from 5PM until dark during Spring, Summer, and Fall. We move inside for practice during the winter months and inclement weather, so the practices can sometimes go a little longer.</p>
                	<p>No experience or equipment is needed to get a youth started in the program and we usually have ammunition available at prices below wholesale. The YSSA competitive team is called the Steel Smashers and is open to any program member. There are funding programs available that help defray costs of competitions and all shooting related items.</p>
                	<p><strong>Equipment used in the program</strong><br />
               	    The YSSA has pistols available for your youth if needed, however, if you'd like to use your own, it has to be either a rim fire or centerfire pistol that meets the IDPA Stock Service Pistol (SSP) requirements. These guidelines can be found in the SPP rules and handbook or by contacting one of the coaches at YSSA.</p>
                	<p><strong>Course of fire</strong><br />
               	    The match consists of 4 stages. At each stage, there are 5 steel targets. These targets are based on guidelines set by the SPP, and can either be 10&quot; or 12&quot; round steel targets, an 18&quot;x24&quot; rectangle steel target, or a combination of these. They are set at various distances from the shooter and the shooter must shoot each target once while being timed. After the shooter successfully shoots each target, the time stops and the shooter will reload and prepare for another round. The shooter will do this 5 times to complete that stage. The highest time is thrown out and the remaining 4 times are added up to give the shooter his or her score for that stage. Once the shooter has completed all 4 stages, his or her and their squad scores (4 shooters usually the same age class) are tallied for a squad total.</p>
                	<p><strong>Age categories for competitive squads</strong><br />
                	  Intermediate Division – Grades 6-8<br />
                	  Senior Division – Grades 9-12<br />
                	  Collegiate Division – Graduated High School and attending a college<br />
               	    Open Division - Combinations of Intermediate and Senior shooters, or Collegiate shooters from different colleges.</p>
           	  <p><strong>How to get involved in the Pistol Program at MRC</strong><br />
               	    Check out YSSA and all the programs they offer on their website <a href="http://www.shootyssa.org">www.shootyssa.org</a>. You can also email YSSA at <a href="mailto:shootyssa@gmail.com">shootyssa@gmail.com</a> or call (309) 945-2108 for additional information on any of the current programs. YSSA offers all types of shooting sports to the youth of the greater Quad Cities and surrounding rural areas. The SPP rules and handbooks can be found on the SSSF website at <a href="http//:www.ssfonline.org" target="_blank">ssfonline.org</a></p>
