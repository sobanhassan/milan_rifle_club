            	 <h1>Bullseye Pistol</h1>
           	  <p>Bullseye Pistol is fired on the Indoor ranges from a distance of either 50 feet up to a distance of 75 feet (25 yards). The indoor course of fire is Slow Fire 10 shots in ten (10) minutes. 10 Shots (10) Timed Fire fired in two strings of 20 seconds each. The targets start from the edged position and will face for 20 seconds. Then Rapid Fire, we call this twice as much fun in half the time. Fire 2 strings of 5 shots each in ten (10) seconds. Once again the targets will start in the edged position and then face for 10 seconds. The Slow fire portion is fired on the B-2 Target and the timed &amp; rapid are both fired on the B-3 target.</p>
           	  <p>The outdoor portion of Bullseye Pistol is fired in same manner except the slow fire is fired on the B-6 target fired from a distance of 50 yards (150 feet) 10 shots in ten (10) minutes. The timed &amp; rapid portion of this match are the same time frames as indoors both 20 &amp; 10 seconds each. These are fired on the B-8 Target on turning targets from a distance of 25 yards (75 feet).</p>
           	  <p>Guns that are legal in this type of competition are anything from factory production guns ranging from .22 caliber to .45 caliber in both semi auto or revolver configuration all the way to full custom pistols &amp; revolvers. Sighting systems allowed are any type of red dot sight that does not project a beam or dot on the target. Persons shooting Iron Sights are allowed a sight radius not to exceed 10 5/8&quot;. Grips allowed are almost unlimited but can't extend up past ¾&quot; inch on the back of the shooting hand. Since Bullseye Pistol is fired from a single hand and unsupported therefore the use of a two handed hold is prohibited in NRA competitions.</p>
           	  <p>Trigger pull rules are on the .22 caliber pistol or revolver the trigger must pick up a minimum weight of 2 pounds. Center fire pistols are required to pick up 3 pounds and revolvers 2.5 pounds. The 45 pistol is required to pick up a minimum weight of 3.5 pounds.</p>
           	  <p>EIC (Excellence in Competition) Hardball Pistols are required to pick up a minimum weight of 4 pounds and must have iron sights and remain factory appearance on the external of the firearm. Unlimited on the internal modifications but all factory safeties must remain. Firearms must either be the 1911 .45 ACP Pistol or the Beretta M-9, 92F or 92FS in 9mm. The 1911 must fire the 230 grain round nose jacketed bullet and the 9mm is either the 115 or 124 grain round nose full metal jacket bullet. No jacketed hollow point bullets are allowed in this competition. Shooters in these competitions are eligible to earn points toward their Distinguished Pistol Badge. The total of points needed to accomplish this is 30.</p>
