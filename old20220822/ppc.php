            	 <h1>Police Pistol Combat (PPC)</h1>
                	<p><img src="images/PPCgraphic.jpg" alt="Conventional shooting target" width="158" height="300" align="right" hspace="5" />Police Pistol Combat is fired during the summer months on the conventional pistol range and during the winter months in the indoor ranges.</p>
                	<p>The course of fire consists of 12 rounds (6 rounds, reload, 6 rounds) at 7 yards in 25 seconds. This is then repeated at 15 yards. Then 6 rounds kneeling, 6 rounds standing right hand supported, and 6 rounds standing left hand supported at 25 yards in 90 seconds. The final course of fire is 6 rounds standing unsupported at 25 yards in 12 seconds. All rounds are fired at the above target.</p>
                	<p>One of the big draws to PPC shooting is the equipment required to compete. There isn't much of it and you decide how much to spend on it. Any out of the box semi-auto or revolver in .22 thru .45 caliber is legal. Three magazines/speed loaders and a holster are also required (holster not required for indoor shooting).</p>
                	<p>Matches are held year round on the first Sunday of the month. Additionally, practice sessions are held every Tuesday at 9:00am</p>
