            	 <h1>Davenport North / West Marine Corps JROTC Air Rifle Team</h1>
                	<p>The Davenport North/West Marine Corps JROTC Air Rifle Team practices regularly every Wednesday, Thursday, and Friday nights at the Milan Rifle Club from 4 pm until 7 pm from the time school starts in August until late April. The cadets will most commonly use in excess of 25k pellets during this time of practice and traveling to different matches throughout the shooting season.</p>
                	<p>Our teams shoot both the Sporter &amp; Precision Air Rifle Class rifles. The course of fire for a 3x10 match is 10 shots fired from the prone position supported by a sling, 10 fired in the standing position unsupported &amp; then 10 shots fired from the kneeling position supported by a sling and kneeling roll. The targets used are the AR 5/10 target fired from a distance of 10 meters (33feet) the diameter of the 10 ring is approximately the size of the head of a pin. Both Sporter &amp; Precision rifles fire .177 caliber pellets.</p>
                	<p>The primary differences between the two classes of rifles is the cost of the equipment. In the Sporter class the rifles can't exceed a cost of $500.00 and the only extra support the shooter has in the class is the use of a web style sling and glove. No shooting coats, pants or boots are allowed in this class. The type(s) of shoes allowed are any shoes that do not extend past the ankle bone. Other than that the shooter is allowed to wear a sweatshirt to help with colder ranges.</p>
                	<p>The Precision class shooters are allowed much more. There is no limit on how much can be spent on a rifle or equipment. The use of shooting coats, pants, boots and different under garments is not required but is highly recommended. Some rifles can exceed $3500.00 without any sights. Shooting in this class takes much more dedication and patience by both the shooter and coaching staff due to the almost unlimited adjustments that can be made to the rifle and shooters clothing and stance.</p>
                	<p>Our teams follow NRA, CMP &amp; ISSF rules. Each of those organizations has very complex sets of rules &amp; regulations that are sometimes hard to follow.</p>
                	<p>Our teams and individuals have traveled to many local matches in West Liberty &amp; Des Moines Iowa as well as National level competitions including The Olympic Training Center in Colorado Springs Colorado, Camp Perry Ohio &amp; Anniston Alabama. These competitions were for advancement in the Marine Corps National Championships with a chance to move on the All Service Championships.</p>
                	<p>In order to be a part of the team one must first be enrolled into the MCJROTC Program at either Davenport North or Davenport West High School. Once enrolled the cadet MUST attend and complete a 12 hour CMP (Civilian Marksmanship Program) safety course which
               	    involves both classroom and supervised range time and then complete a written test as well as a 30 shot rifle course fired 10 rounds prone, 10 rounds standing &amp; 10 rounds kneeling. As coaches we will then usually pick the top four (4) shooters and invite them to join the team on a trial basis. There is no cost to the shooter or parents to join the team and shoot all we ask for is 110% effort from the cadet and to remain positive.</p>
