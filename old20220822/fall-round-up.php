            	 <h1>Illowa Irregulars Fall Round-Up</h1>
                	<p>Fall Round-Up is the Illowa Irregulars annual club shoot which is held on the last weekend (Friday, Saturday, Sunday) of September each year. Pre-registration is required for this shoot (<a href="docs/FRU20Application.pdf" target="_blank">application here</a>).<br />
               	    Friday is side matches, which are a group of &quot;fun and fast&quot; targets that are set up to allow participants to just come out and have some fun. Side matches include speed pistol, speed rifle (running buffalo), speed derringer, speed pocket pistol, 50 yard pistol, 100 yard rifle (pistol caliber), 100 yard rifle (rifle caliber), 300 yard long range rifle, Texas star (pistol), Texas star (shotgun), and two-man/woman team events called Dapper's Revenge and Granny Annie's Revenge (pistol and shotgun).</p>
                	<p>
                	  Saturday and Sunday are the main match days - 5 stages are shot each day with lunch served after shooting is done.</p>
                	<p>
                	  A banquet is held Saturday evening in the club's indoor range.</p>
                	<p>
                	  Awards ceremony is held Sunday after lunch.</p>
