            	 <h1>Air Pistol Silhouette</h1>
                
                	<p>A silhouette is a steel profile of an animal (chicken, pig, turkey, or ram) standing on a pedestal. The object of the game is to knock as many silhouettes as possible off their pedestals, shooting one round at each silhouette. It is a very simple competition, with very simple scoring: You get a 1 for knocking an animal off its stand, a 0 for anything else. Although silhouette is an easy game to learn, it is not an easy game to master. The animals are rather small and the distances are rather large and you must shoot offhand (standing). A match consists of 40 shots (10 of each animal).</p>
                     <p> One of the appeals of silhouette shooting is the gear. There isn't much of it, and you can choose how much money to spend on it. Most individuals use a .177 cal (.22 cal pellet is max) pellet pistol with either a scope or red dot for sighting. Ammunition (pellets) for these pistols is relatively cheap and readily available.</p>
                   <p>Matches are held September through June on the second Sunday of the
month at 9:00 am. In addition, weekly practice sessions are held every
Wednesday morning starting at 9:00am all year long.</p>
