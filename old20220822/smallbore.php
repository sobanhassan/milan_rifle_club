                	<p>Milan offers a junior rifle program that is open to juniors ages 11 through high school age (individuals do not need to be a member of the Milan Rifle Club to participate). We teach Olympic and collegiate style position smallbore rifle and Precision Air rifle. We follow the NRA 4-Position Qualification Program. We are able to provide most of the equipment needed for the beginning thru the intermediate skill levels. This program is run from the middle of September thru April, on Wednesday nights (6:00 to 8:00). This program has been very successful with numerous juniors going on to shoot on college and Academy rifle teams such as Texas Christian University, University of Tennessee - Martin, Murray State, U.S. Military Academy (West Point), U.S. Air Force Academy, Eastern New Mexico, Marquette University. More info can be found here:<br />
