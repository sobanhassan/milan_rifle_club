               	  <h4>Q. How do I become a member?</h4>
                    
                    <ol type="1">
                    	<li>You must attend an orientation meeting.  Call Terry Spurrier at 309-798-2635 to register for an orientation.</li>
                    	<li>You must be a member of the "National Rifle Association". You can sign up for the NRA at the orientation meeting.</li>
                        <li>All memberships are yearly from January 1 to December 31 of the same year.</li>
                    </ol>
                
                	<h4>Q. Do we rent the ranges or facilities?</h4>
                    
                    <p style="margin-left:20px">A. No, the ranges are for members use only. Sometimes we rent club ranges to the local Armed Forces Units.</p>
                    
                    <h4>Q. Do we hold concealed carry classes at the club?</h4>
                    
                    <p style="margin-left:20px">A. No, in April and October we hold an "NRA Basic Pistol Class" for members only. This class will allow you to get an Iowa concealed carry permit. This class will also count for 8 hours of class time toward an Illinois concealed carry permit.</p>
                    
                    <h4>Q. What facilities does the club have?</h4>
                    
                    <p style="margin-left:20px">A. We have:</p>
                    <ol type="1">
                    	<li>2 indoor pistol ranges with indoor restrooms (25 yards and 50 foot).</li>
                    	<li>An outdoor conventional pistol range (25 & 50 yards).</li>
                        <li>A 600, 300 and 200 yard rifle range.</li>
                        <li>A benchrest range (targets can be set at 25, 50, 100 and 180 yards).</li>
                        <li>5 walled ranges whose distances vary from 40 yards to 10 yards (you decide what distance to shoot at).</li>
                        <li>10 competition bays that are only used for club sponsored matches and practices.</li>
                    </ol>
                    
                    <h4>Q. What does the membership cost?</h4>
                    
                    <p style="margin-left:20px">A. 18 to 61 years of age $175 a year, over 61 years of age $75 a year. There are special rates for juniors, spouses, military personnel and full time students. NRA membership is required at an additional cost.</p> 
