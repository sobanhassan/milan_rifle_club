          <li>Guns shall NOT  be fired in fully automatic OR burst mode on club property.</li>
          <li>Remove and  properly stow or dispose of all targets when you are through shooting.</li>
          <li>Remove and  properly stow all target frames and/or hangers when shooting is completed.</li>
