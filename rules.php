<p><strong>GENERAL RANGE RULES</strong></p>
<ol>
  <li>No outdoor shooting before sunrise or after sunset.</li>
  <li>No shooting on ALL outdoor ranges other than .22 rimfire  before 8:00 AM.</li>
  <li>Do not shoot diagonally across the ranges. Shoot at  designated backstops from designated firing points.</li>
  <li>Do not shoot at targets on the ground. Place all targets  in such a manner so as to ensure the bullets hit the impact area and do not  ricochet. Refer to Rule #12 in Universal Range Rules.</li>
  <li>No vehicles are permitted on any firing lines or berms.</li>
  <li>Any legal firearm and ammunition EXCEPT tracers,  incendiary, 50 BMG and armor piecing may be used on club property. Some  specific ranges have firearm and/or ammunition restrictions.</li>
  <li>During work days or for certain outdoor matches, all  outdoor ranges may be closed to both scheduled and open shooting. See the club  calendar or the club website at www.milanrifle.club for further details or  other club activities.</li>
  <li>Camping is allowed in approved areas only. Campsites must  be removed in 72 hours unless the campers are participating in a match or other  club activity.</li>
  <li>Club owned metal targets may only be fired at with any  pistol, muzzleloader or straight walled center fire rifles loaded with ALL LEAD  BULLETS. Do not shoot at metal targets with center fire rifles loaded with  jacketed bullets.</li>
  <li>During a scheduled range time if no one is using the  scheduled range, the conflicting range may be used. If someone arrives to use  the scheduled range, the use of the conflicting range must cease immediately.</li>
  <li>Only shotgun shells from 7½ to 9 size shot may be fired  at clay birds or during any competition.</li>
  <li>ANY RULE may be waived for certain organized events with  prior Board approval.</li>
  <li>A chamber safety flag SHALL be used in ALL firearms, both  uncased and on the line before declaring the firing line safe.</li>
  <li>No firearm shall be brought to or leave the firing line  loaded. If you are carrying a concealed firearm and it is NOT the gun you are  shooting it MUST remain concealed. If it IS the firearm you are going to shoot  it can’t be brought to the line loaded. It must be made safe BEFORE entering  the range, either cased or boxed and have an empty chamber indicator installed.</li>
  <li>All firearms must  comply with range rules and range usage. </li>
</ol>
