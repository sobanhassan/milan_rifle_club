        <h1>Frequently Asked Questions</h1>
        <h4>Q. How do I become a member?</h4>
        <ol type="1">
          <li>You must attend an orientation meeting. Call Terry Spurrier at 309-798-2635 to register for an orientation.</li>
          <li>You must be a member of the &quot;National Rifle Association&quot;. You can sign up for the NRA at the orientation meeting.</li>
          <li>All memberships are yearly from January 1 to December 31 of the same year.</li>
        </ol>
        <br />
        <h4>Q. Do we rent the ranges or facilities?</h4>
        <p>A. No, the ranges are for members use only. Sometimes we rent club ranges to the local Armed Forces Units.</p>
        <h4>Q. Do we hold concealed carry classes at the club?</h4>
        <p>A. No, in April and October we hold an &quot;NRA Basic Pistol Class&quot; for members only. This class will allow you to get an Iowa concealed carry permit. This class will also count for 8 hours of class time toward an Illinois concealed carry permit.</p>
        <h4>Q. What facilities does the club have?</h4>
        <p>A. We have:<br />
          - 2 indoor pistol ranges with indoor restrooms (25 yards and 50 foot).<br />
          - An outdoor conventional pistol range (25 &amp; 50 yards).<br />
          - A 600, 300 and 200 yard rifle range.<br />
          - A smallbore / airgun rifle range.<br />
          - A benchrest range (targets can be set at 25, 50, 100 and 180 yards).<br />
          - 5 walled ranges whose distances vary from 40 yards to 10 yards (you decide what distance to shoot at).<br />
          - 10 competition bays that are only used for club sponsored matches and practices.<br />

        <h4>Q. What does the membership cost?</h4>
        <p>A. 18 to 61 years of age $175 a year, over 61 years of age $75 a year. There are special rates for juniors, spouses, military personnel and full time students. NRA membership is required at an additional cost.</p>
        
        <h4>Q. Can conflicting ranges be used at the same time?</h4>
        <p>A. The Milan Rifle Club does not allow the use of the 200yd and/or smallbore ranges when the 600yd or 300yd range is in use. Conversely, the 600yd or 300yd range can not be used when the 200yd and/or smallbore ranges are in use. The 200yd and smallbore ranges can be used at the same time. For example:
            <ul>
                <li>If the 600yd range is in use, the 300yd, 200yd, and smallbore ranges are closed</li>
                <li>If the 300yd range is in use, the 600yd, 200yd, and smallbore ranges are closed</li>
                <li>If the 200yd and/or the smallbore range is in use, the 600yd and 300yd ranges are closed.</li>
        <h4>Q. What Does the One-hour Rule Mean?</h4>
        <p>A. Any member may invoke the one-hour rule, except during matches or formal practices. The one-hour rule applies to full as well as conflicting ranges. For a full firing line (Indoor, Benchrest, and Walled Ranges), the one-hour rule allows a member to ask that a shooter(s) finish their shooting in the next 60 minutes to allow the waiting shooter a spot on the line. To use a conflicting range (200 yd, 300 yd, 600 yd, and Smallbore Ranges) the one-hour rule allows a member to ask that all shooters finish their shooting in the next 60 minutes to allow the waiting shooter an opportunity to use the conflicting range.</p>
